# Step 1 — Rainfall (India, 1901-2015): melt to monthly, add COUNTRY, DATE

rainfall_path = Path("/content/rainfall in india 1901-2015.csv")
rf = pd.read_csv(rainfall_path)

# drop redundant 'index' if present, normalize cols
if "index" in rf.columns: rf = rf.drop(columns=["index"])
rf.columns = rf.columns.str.strip().str.upper()

# melt months to long
month_cols = ["JAN","FEB","MAR","APR","MAY","JUN","JUL","AUG","SEP","OCT","NOV","DEC"]
have_months = [c for c in month_cols if c in rf.columns]

rf_long = rf.melt(
    id_vars=[c for c in ["SUBDIVISION","YEAR"] if c in rf.columns],
    value_vars=have_months,
    var_name="MONTH",
    value_name="RAINFALL_MM"
)

# fill NaNs subdivision-wise
grp_cols = [c for c in ["SUBDIVISION"] if c in rf_long.columns]
rf_long["RAINFALL_MM"] = rf_long.groupby(grp_cols)["RAINFALL_MM"].transform(
    lambda s: s.fillna(s.mean())
)

# month number & DATE (use first day of month)
rf_long["MONTH_NUM"] = rf_long["MONTH"].map(MONTH_ABBR_TO_NUM).astype(int)
rf_long["DATE"] = pd.to_datetime(
    rf_long["YEAR"].astype(int).astype(str) + "-" + rf_long["MONTH_NUM"].astype(int).astype(str) + "-01"
).dt.to_period("M").dt.start_time

# country column (all India)
rf_long["COUNTRY"] = "India"

# (Optional) aggregate to country-month (sum or mean).
# Rainfall is additive across regions; use mean if you want average rainfall instead.
rf_india = (rf_long
            .groupby(["COUNTRY","DATE"], as_index=False)
            .agg(RAINFALL_MM=("RAINFALL_MM","sum")))

