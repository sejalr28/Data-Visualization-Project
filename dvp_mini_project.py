# --- helpers ---
MONTH_ABBR_TO_NUM = {m[:3].upper(): i for i, m in enumerate(calendar.month_name) if m}

def to_month_start(dt_series: pd.Series) -> pd.Series:
    dt = pd.to_datetime(dt_series, errors="coerce", utc=False)
    dt = dt.dt.tz_localize(None, ambiguous='NaT', nonexistent='NaT') if hasattr(dt, "dt") else dt
    return pd.to_datetime(dt.dt.to_period("M").dt.start_time)

def guess_date_col(df: pd.DataFrame):
    cand = [c for c in df.columns if c.lower() in
            {"dt","date","time","timestamp","datetime","formatted date","formatted_date","date_time"}]
    return cand[0] if cand else None

def standardize_country(col: pd.Series) -> pd.Series:
    if col is None:
        return None
    return (col.astype(str)
            .str.strip()
            .str.replace(r"\s+", " ", regex=True)
            .str.title())

def pick_first_existing(df: pd.DataFrame, names):
    for n in names:
        if n in df.columns:
            return n
    return None

def season_from_month(m):
    # Simple India-centric seasons; adjust if needed
    return (
        "Winter" if m in (12,1,2) else
        "Pre-Monsoon" if m in (3,4,5) else
        "Monsoon" if m in (6,7,8,9) else
        "Post-Monsoon"
    )

